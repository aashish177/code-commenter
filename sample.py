def divide(a, b):
    """This function takes two numbers, a and b, as input and returns the result of dividing a by b."""
    return a / b

def power(a, b):
    '''"""
Calculate the power of 'a' raised to the exponent 'b'.
:param a: base number
:param b: exponent
:return: power of 'a' raised to 'b'
"""'''
    return a ** b