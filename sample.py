def divide(a, b):
    '''"""Return the result of dividing number a by number b.

Parameters:
a (float): The dividend.
b (float): The divisor, cannot be zero.

Returns:
float: The result of the division.

Raises:
ZeroDivisionError: If b is equal to zero.
"""'''
    return a / b

def power(a, b):
    """Calculate the value of 'a' raised to the power of 'b'. 

:param a: base number
:param b: exponent number
:return: result of 'a' raised to the power of 'b'"""
    return a ** b