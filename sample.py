def add(a, b):
    """'''Adds two numbers together and returns the sum.

Args:
    a (int or float): The first number to add.
    b (int or float): The second number to add.

Returns:
    int or float: The sum of a and b.
'''"""
    return a + b

def subtract(a, b):
    '''"""Returns the result of subtracting b from a.

    Args:
        a (int): The number from which b will be subtracted.
        b (int): The number to subtract from a.

    Returns:
        int: The result of subtracting b from a.
"""'''
    return a - b

def multiply(a, b):
    '''"""Multiply two numbers and return the result.

Args:
    a (int): The first number to be multiplied.
    b (int): The second number to be multiplied.

Returns:
    int: The product of the two numbers.
"""'''
    return a * b